# Page on which the game is played, each player has a button associated to his name and once the button is clicked, the player can see his challenge.
# He can then close the window and start the challenge. 
# The player look one after each other their challenges once the game has started.

# Once he completed the challenge, he can press another button right next to the previous one and confirm that he succided, and the value 
# of the challenge is added to his score.
# Once the time is up (1h in general) all the challenges are refreshed and it can start again.